package com.sb.example.demo.controllers;




public class BalanceSheetBean {

	private String month;
	private String profitOrLoss;
	private String assetsValue;
	private String year;
	public String getMonth() {
		return month;
	}
	public void setMonth(String month) {
		this.month = month;
	}
	public String getProfitOrLoss() {
		return profitOrLoss;
	}
	public void setProfitOrLoss(String profitOrLoss) {
		this.profitOrLoss = profitOrLoss;
	}
	public String getAssetsValue() {
		return assetsValue;
	}
	public void setAssetsValue(String assetsValue) {
		this.assetsValue = assetsValue;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}

	
}
